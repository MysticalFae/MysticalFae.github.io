// removing and adding values from/to arrays

let myArray = [1, 2, 3, 4, 5];

console.log(myArray[2]); //will print value in 2nd index which is 3

//push
myArray.push(6); // pushing/adding '6' into the array (added to the end of the array)
console.log(myArray.toString());

//pop
console.log(myArray.pop()); // will automatically pop off the last value and print it out to u
console.log(myArray.toString());
